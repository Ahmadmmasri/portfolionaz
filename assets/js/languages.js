            $('input#email').attr('placeholder', 'E-posta adresiniz');
            $('input#subject').attr('placeholder', 'Ders');
            $('textarea').attr('placeholder', 'İleti');
            $('div.portfolio-links a[ title="Portfolio Details"]').attr("title", "Portföy Detayları")





